    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Product::class);
    }

    /**
     * @return Product[] Returns an array of Product objects
     */
    public function findProductByWhatSearched($searched)
    {
        return $this->createQueryBuilder('p')
            ->where('p.title LIKE :val')
            ->setParameter('val', '%'.$searched.'%')
            ->orWhere('p.description LIKE :val2')
            ->setParameter('val2', '%'.$searched.'%')
            ->andWhere('p.isAvailable = :val3')
            ->setParameter('val3', true)
            ->getQuery()
            ->getResult()
        ;
    }

